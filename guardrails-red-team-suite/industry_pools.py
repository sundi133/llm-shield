from __future__ import annotations
import hashlib

def stable_index(modulo: int, *parts: str) -> int:
    if modulo <= 0:
        return 0
    digest = hashlib.sha256("|".join(parts).encode("utf-8")).hexdigest()
    return int(digest, 16) % modulo

